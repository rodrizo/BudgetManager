    void Modificar() {

        String categoria = CategoriaBox.getSelectedItem().toString();
        Float cantidad = Float.parseFloat(txtCantidad.getText());
        int id_bud = Integer.parseInt(txtBudget.getText());
        int id = Integer.parseInt(txtDebito.getText());

        String sql = "UPDATE debitos SET Categoria='" + categoria + "',Cantidad='" + cantidad + "' ,IdBudget='" + id_bud + "' WHERE IdDebito=" + id;
        try {
            if (categoria != null || cantidad == null || id_bud != 0 || id != 0) {
                con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Presupuesto modificado con éxito");
                limpiarTabla(model);

            } else {
                JOptionPane.showMessageDialog(null, "Error...!!!");
            }

        } catch (Exception e) {
        }
    }

    void Agregar() {
        String categoria = CategoriaBox.getSelectedItem().toString();
        Float cantidad = Float.parseFloat(txtCantidad.getText());
        int id_bud = Integer.parseInt(txtBudget.getText());

        try {
            if (categoria.equals("") || cantidad == null) {
                JOptionPane.showMessageDialog(null, "Debe ingresar datos del presupuesto");
                limpiarTabla(model);
            } else {
                String sql = "INSERT INTO debitos(Categoria,Cantidad,IdBudget) VALUES('" + categoria + "','" + cantidad + "','" + id_bud + "')";
                //restando el débito con el monto
                //it'll work as a trigger
                String sql2 = "UPDATE presupuesto INNER JOIN debitos on (presupuesto.Id =" + id_bud + ") = (debitos.IdBudget = " + id_bud + ") SET presupuesto.Monto = presupuesto.Monto - " + cantidad + "WHERE presupuesto.Id=" + id_bud + " && debitos.IdDebito=LAST_INSERT_ID()";
                con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql);
                st.executeUpdate(sql2);
                JOptionPane.showMessageDialog(null, "Débito registrado con exito");
                limpiarTabla(model);

            }

        } catch (Exception e) {
        }

    }

    void limpiarTabla(DefaultTableModel model) {
        for (int i = 0; i <= DebitTable.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }

