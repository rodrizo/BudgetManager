    private void DebitTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DebitTableMouseClicked

        int row = DebitTable.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(null, "No se seleccionó ningún presupuesto");
        } else {
            id = Integer.parseInt((String) DebitTable.getValueAt(row, 0).toString());
            String categoria = (String) DebitTable.getValueAt(row, 1).toString();
            String cantidad = DebitTable.getValueAt(row, 2).toString();

            //setting in textfields
            txtDebito.setText("" + id);
            CategoriaBox.setSelectedItem(categoria);
            txtCantidad.setText(cantidad);
        }

    }//GEN-LAST:event_DebitTableMouseClicked

