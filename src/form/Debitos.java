    void listar() {

        int id_Budget = Integer.parseInt(txtBudget.getText());

        String sql = "SELECT * FROM debitos WHERE debitos.IdBudget =" + id_Budget;
        try {
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            Object[] budget = new Object[3];
            model = (DefaultTableModel) DebitTable.getModel();
            while (rs.next()) {
                budget[0] = rs.getInt(1);
                budget[1] = rs.getString(2);
                budget[2] = rs.getFloat(3);
                model.addRow(budget);
            }
            DebitTable.setModel(model);

        } catch (Exception e) {
        }

    }


